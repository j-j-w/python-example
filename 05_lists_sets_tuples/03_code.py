# lists
l = ['Apple', 'Banana', 'Mango']

# Add en element to the end of the list
l.append('Pineapple')
print(l)

# Remove en element from the list by value
l.remove('Banana')
print(l)

l[0] = 'Orange'
print(l)


# Sets
s = {'Apple', 'Banana', 'Mango'}

# Add en element to the list 
s.add('Orange')
print(s)


# Tuples
t = ('Apple', 'Banana', 'Mango')
# tuples is not allow modify elements

print(t)
